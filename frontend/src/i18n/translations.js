const setNestedValue = (target, path, value) => {
  const parts = path.split('.');
  let current = target;

  parts.forEach((part, index) => {
    if (index === parts.length - 1) {
      current[part] = value;
      return;
    }

    if (!current[part]) {
      current[part] = {};
    }

    current = current[part];
  });

  return target;
};

